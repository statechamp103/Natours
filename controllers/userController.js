const User = require('../models/userModel');

exports.getAllUsers = async (req, res, next) => {
  try {
    // EXECUTE QUERY
    const users = await User.find();

    if (req.query.page) {
      if (!users.length) throw new Error('This page does not exist');
    }

    // SEND RESPONSE
    res.status(200).json({
      status: 'success',
      results: users.length,
      data: {
        users,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err.message,
    });
  }
};

